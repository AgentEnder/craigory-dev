// Generates the Unicode "age" (version-introduced) dataset and a name supplement
// for code points newer than the installed @unicode/unicode-N names package.
//
// THIS SCRIPT IS THE VERSION-BUMP MECHANISM. It downloads the Unicode Character
// Database directly from unicode.org. By default it pulls the *draft* UCD, which
// during a beta review period is the next (unreleased) version — currently 18.0.
//
//   pnpm --filter alt-codes gen:unicode               # draft (latest, incl. beta)
//   UCD_BASE_URL=https://www.unicode.org/Public/17.0.0/ucd pnpm --filter alt-codes gen:unicode
//
// Outputs (committed .ts modules so they bundle cleanly in every context — dev server,
// vite.config plugins, and the prerender server bundle — and stay offline + deterministic):
//   src/generated/unicode-age.ts   — ageRanges: sorted [startCp, endCp, "version"], ALL versions
//   src/generated/draft-names.ts   — draftNames: { "<hexCp>": ["NAME", "<generalCategory>"] }
//                                     only for code points the names package lacks
//   src/generated/meta.ts          — generatedMeta: { ucdVersion, ucdSource, latestVersion }
//   src/generated/emoji.ts         — emojiData: ordered base emoji with group/subgroup/
//                                     emojiVersion/skinToneSupport (replaces unicode-emoji-json)
//
// Data is embedded via JSON.parse(<string literal>) rather than a JSON import, so tsc never
// has to infer a 13k-property literal type.

import { writeFileSync, mkdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { createRequire } from 'node:module';

const requireJson = createRequire(import.meta.url);
// The installed names package — anything it already names is omitted from the supplement.
const unicodeNames = requireJson('@unicode/unicode-17.0.0/Names/index.js');

const BASE = process.env.UCD_BASE_URL ?? 'https://www.unicode.org/Public/draft/ucd';
// emoji-test.txt sits beside the ucd dir (…/draft/emoji/…), not under it.
const EMOJI_TEST_URL =
  process.env.EMOJI_TEST_URL ?? BASE.replace(/\/ucd$/, '/emoji') + '/emoji-test.txt';
const OUT_DIR = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'generated');

const SKIN_TONE = new Set([0x1f3fb, 0x1f3fc, 0x1f3fd, 0x1f3fe, 0x1f3ff]);

/** Write a committed .ts module that embeds `value` via JSON.parse(<escaped literal>). */
function emitModule(filename, exportName, value, typeAnnotation) {
  const literal = JSON.stringify(JSON.stringify(value));
  const content =
    `// AUTO-GENERATED by scripts/generate-unicode-data.mjs — do not edit by hand.\n` +
    `export const ${exportName}: ${typeAnnotation} = JSON.parse(${literal});\n`;
  writeFileSync(join(OUT_DIR, filename), content);
}

async function fetchUrl(url) {
  const res = await fetch(url, { headers: { 'User-Agent': 'glyph-index-build' } });
  if (!res.ok) throw new Error(`Failed to fetch ${url}: ${res.status} ${res.statusText}`);
  return res.text();
}

const fetchText = (file) => fetchUrl(`${BASE}/${file}`);

/** Parse emoji-data.txt → set of Emoji_Modifier_Base code points (skin-tone bases). */
function parseModifierBases(text) {
  const set = new Set();
  for (const raw of text.split('\n')) {
    const line = raw.split('#')[0];
    if (!line.includes('Emoji_Modifier_Base')) continue;
    const m = line.match(/^([0-9A-Fa-f]+)(?:\.\.([0-9A-Fa-f]+))?/);
    if (!m) continue;
    const start = parseInt(m[1], 16);
    const end = m[2] ? parseInt(m[2], 16) : start;
    for (let cp = start; cp <= end; cp++) set.add(cp);
  }
  return set;
}

/**
 * Parse emoji-test.txt → ordered base emoji with group/subgroup/version/name.
 * Mirrors unicode-emoji-json's set: fully-qualified only, with the Component group and
 * skin-tone variants excluded; skin-tone support is derived from Emoji_Modifier_Base.
 */
function parseEmojiTest(text, modifierBases) {
  const out = [];
  let group = '';
  let subgroup = '';
  for (const raw of text.split('\n')) {
    const gm = raw.match(/^# group:\s*(.+?)\s*$/);
    if (gm) { group = gm[1]; continue; }
    const sm = raw.match(/^# subgroup:\s*(.+?)\s*$/);
    if (sm) { subgroup = sm[1]; continue; }
    if (group === 'Component') continue;
    const m = raw.match(/^([0-9A-Fa-f ]+?)\s*;\s*fully-qualified\s*#\s*\S+\s+E([0-9.]+)\s+(.+?)\s*$/);
    if (!m) continue;
    const cp = m[1].trim().split(/\s+/).map((h) => parseInt(h, 16));
    if (cp.some((c) => SKIN_TONE.has(c))) continue; // a skin-tone variant of a base emoji
    out.push({
      cp,
      name: m[3],
      group,
      subgroup,
      emojiVersion: m[2],
      skinToneSupport: cp.some((c) => modifierBases.has(c)),
    });
  }
  return out;
}

/** Parse DerivedAge.txt → sorted array of [startCp, endCp, version]. */
function parseAge(text) {
  const ranges = [];
  let ucdVersion = 'unknown';
  for (const raw of text.split('\n')) {
    const headerMatch = raw.match(/^# DerivedAge-([0-9.]+)\.txt/);
    if (headerMatch) ucdVersion = headerMatch[1];
    const line = raw.split('#')[0].trim();
    if (!line) continue;
    const m = line.match(/^([0-9A-Fa-f]+)(?:\.\.([0-9A-Fa-f]+))?\s*;\s*([0-9]+\.[0-9]+)/);
    if (!m) continue;
    const start = parseInt(m[1], 16);
    const end = m[2] ? parseInt(m[2], 16) : start;
    ranges.push([start, end, m[3]]);
  }
  ranges.sort((a, b) => a[0] - b[0]);
  return { ranges, ucdVersion };
}

/**
 * Parse UnicodeData.txt into a { cp -> [name, generalCategory] } map, but ONLY for
 * the requested code points. Algorithmic First/Last ranges (CJK, Seal, Jurchen, …)
 * are expanded using the standard "<Label, First>" → "LABEL-XXXX" naming rule.
 */
function parseNames(text, wantedCps) {
  const out = {};
  const lines = text.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const fields = lines[i].split(';');
    if (fields.length < 3) continue;
    const cp = parseInt(fields[0], 16);
    const name = fields[1];
    const gc = fields[2];

    if (name.endsWith(', First>')) {
      // Range: name on this line is the marker, the next line carries the range end.
      const label = name.slice(1, -', First>'.length).toUpperCase();
      const lastFields = lines[++i].split(';');
      const end = parseInt(lastFields[0], 16);
      for (let c = cp; c <= end; c++) {
        if (!wantedCps.has(c)) continue;
        out[c.toString(16).toUpperCase()] = [
          `${label}-${c.toString(16).toUpperCase().padStart(4, '0')}`,
          gc,
        ];
      }
      continue;
    }

    // Skip code-point-type markers like <control> — they have no public name.
    if (name.startsWith('<')) continue;
    if (!wantedCps.has(cp)) continue;
    out[cp.toString(16).toUpperCase()] = [name, gc];
  }
  return out;
}

async function main() {
  console.log(`Fetching UCD from ${BASE} …`);
  const [ageText, unicodeDataText, emojiTestText, emojiDataText] = await Promise.all([
    fetchText('DerivedAge.txt'),
    fetchText('UnicodeData.txt'),
    fetchUrl(EMOJI_TEST_URL),
    fetchText('emoji/emoji-data.txt'),
  ]);

  const { ranges, ucdVersion } = parseAge(ageText);
  console.log(`Parsed ${ranges.length} age ranges (UCD ${ucdVersion}).`);

  const emoji = parseEmojiTest(emojiTestText, parseModifierBases(emojiDataText));
  console.log(`Parsed ${emoji.length} base emoji.`);

  // The supplement only needs names the installed package can't provide. In practice
  // that's everything introduced in the draft version (and any later additions).
  const newest = ucdVersion.replace(/\.0$/, ''); // "18.0.0" → "18.0" as used in DerivedAge
  // Fetching from the …/Public/draft/… tree means `newest` is the next, UNRELEASED
  // version (beta). Pinning UCD_BASE_URL to a released version (…/17.0.0/ucd) clears this.
  const draftVersion = /\/draft(\/|$)/.test(BASE) ? newest : null;
  const supplementCps = new Set();
  for (const [start, end] of ranges) {
    // Keep names for any code point the installed names package doesn't know.
    for (let cp = start; cp <= end; cp++) {
      if (!unicodeNames.has(cp)) supplementCps.add(cp);
    }
  }
  console.log(`Resolving names for ${supplementCps.size} code points missing from the package …`);

  const draftNames = parseNames(unicodeDataText, supplementCps);

  mkdirSync(OUT_DIR, { recursive: true });
  emitModule('unicode-age.ts', 'ageRanges', ranges, 'Array<[number, number, string]>');
  emitModule('draft-names.ts', 'draftNames', draftNames, 'Record<string, [string, string]>');
  emitModule(
    'meta.ts',
    'generatedMeta',
    { ucdVersion, ucdSource: BASE, latestVersion: newest, draftVersion },
    '{ ucdVersion: string; ucdSource: string; latestVersion: string; draftVersion: string | null }',
  );
  emitModule(
    'emoji.ts',
    'emojiData',
    emoji,
    'Array<{ cp: number[]; name: string; group: string; subgroup: string; emojiVersion: string; skinToneSupport: boolean }>',
  );

  console.log(
    `Wrote ${ranges.length} ranges, ${Object.keys(draftNames).length} supplement names, and ${emoji.length} emoji to src/generated/.`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
